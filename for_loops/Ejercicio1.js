for(var i = 0; i < 5; i++) {
    console.log(i);
}

//Para el uso del for, debemos de escribir for() y entre los paréntesis escribir una variable, la condición y lo que queremos que haga dicha variable.
//En este caso, estamos definiendo una variable i y le estamos dando el valor inicial de 0 (lo cuál suele ser lo más común).
//Luego le decimos, mientras i sea (puede ser mayor qué, menor qué, igual a... etc. En este caso es menor qué) 5, que se sume en 1 su valor.
//Se puede escribir i+1 o, como se ve en el ejemplo, i++ para decirle a la variables que aumente su valor de 1 en 1.

//Otra cosa a destacar, es que se muestra del 0 al 4, ya que no estamos diciendo i <= 5, sino, i < 5.
//Por lo que una vez i sea = 5, ya no muestra el resultado porque no es lo que le estamos especificando en las condiciones.